package LinkedList;

public class SinglyNode {

	private int data;
	private SinglyNode next;

	public SinglyNode() {
	}

	public int getData() {
		return data;
	}

	public void setData(int data) {
		this.data = data;
	}

	public SinglyNode getNext() {
		return next;
	}

	public void setNext(SinglyNode next) {
		this.next = next;
	}
}
